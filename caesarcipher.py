alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l',
            'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
def caesar(word,shift,direction):
    if shift > len(alphabet)-1:
        shift =  shift%len(alphabet)

    text = ""
    for l in word:
        if l not in alphabet:
            text += l
        else:
            p = alphabet.index(l)
            if direction == "encode":
                nw_p = p + shift
                if nw_p <= (len(alphabet)-1):
                    nw_p = nw_p
                else:
                    nw_p = nw_p-(len(alphabet))
            else:
                nw_p = p - shift
            nw_l = alphabet[nw_p]
            text += nw_l
    print(f"The {direction}d text is {text}")








